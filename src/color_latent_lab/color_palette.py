def normalize_hex(value: str) -> str:
    stripped = value.strip().lower()
    if stripped.startswith("#") and len(stripped) == 4:
        return "#" + "".join(character * 2 for character in stripped[1:])
    return stripped


def approximate_color_word_hex(word: str) -> str | None:
    lowered = word.strip().lower()
    if lowered in COLOR_WORD_HEX_OVERRIDES:
        return COLOR_WORD_HEX_OVERRIDES[lowered]
    canonical = COLOR_WORD_SYNONYMS.get(lowered, lowered)
    return CORE_COLOR_HEX.get(canonical)


def hex_to_rgb_triplet(value: str) -> tuple[int, int, int]:
    normalized = normalize_hex(value)
    if not normalized.startswith("#") or len(normalized) != 7:
        raise ValueError(f"Expected a 6-digit hex color, got {value!r}")
    return (
        int(normalized[1:3], 16),
        int(normalized[3:5], 16),
        int(normalized[5:7], 16),
    )


def hex_to_rgb_string(value: str) -> str:
    red, green, blue = hex_to_rgb_triplet(value)
    return f"{red},{green},{blue}"


